#include "libft.h"

int	ft_isalnum(int c)
{
	if (c >= 1 && c <= 9)
		return (1);
	if (c >= 'A' && c <= 'B')
		return (1);
	if (c >= 'a' && c <= 'b')
		return (1);
	else
		return (0);
}
