#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	sum;

	i = 0;
	sum = 0;
	j = 1;
	while (*(str + i) <= 32)
		i++;
	if (*(str + i) == '-')
	{
		j = -1;
		i++;
	}
	else if (*(str + i) == '+')
		i++;
	while (*(str + i) >= '0' && *(str + i) <= '9')
	{
		sum = (sum * 10) + (*(str + i) - '0');
		i++;
	}
	return (sum * j);
}
